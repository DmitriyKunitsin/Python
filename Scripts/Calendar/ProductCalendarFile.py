import datetime

class ProductionCalendar:
    """Производственный календарь: праздники и переносы."""
    
    # Праздничные дни (фиксированные даты)
    HOLIDAYS = {
        (2026, 1, 1), (2026, 1, 2), (2026, 1, 7),  # Новогодние каникулы, Рождество
        (2026, 2, 23),  # День защитника Отечества
        (2026, 3, 8),   # Международный женский день
        (2026, 5, 1), (2026, 5, 9),  # Первомай, День Победы
        (2026, 6, 12),  # День России
        (2026, 11, 4),  # День народного единства
    }
    
    # Переносы выходных: { (год, месяц, день): True/False }
    # True – рабочий день (обычно суббота), False – выходной (обычно понедельник/пятница)
    TRANSFERS = {
        # Примеры для 2026 года (реальные даты нужно уточнить)
        # (2026, 1, 3): True,  # рабочая суббота за понедельник 5 января
        # (2026, 5, 4): False, # выходной понедельник за субботу 2 мая
    }

    @classmethod
    def is_working_day(cls, year: int, month: int, day: int) -> bool:
        """Проверяет, является ли день рабочим по производственному календарю."""
        date_key = (year, month, day)
        # Проверяем переносы в первую очередь
        if date_key in cls.TRANSFERS:
            return cls.TRANSFERS[date_key]
        # Если день выпадает на субботу или воскресенье – нерабочий (если не перенос)
        dt = datetime.date(year, month, day)
        if dt.weekday() >= 5:  # 5=Сб, 6=Вс
            return False
        # Праздничный день – нерабочий
        if date_key in cls.HOLIDAYS:
            return False
        return True