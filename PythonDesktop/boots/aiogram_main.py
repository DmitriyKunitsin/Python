import aiogram
import asyncio  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–π—Ç–µ asyncio
print(aiogram.__version__)
from aiogram import Bot, Dispatcher, types
from aiogram.dispatcher.fsm import FSMContext
from aiogram.types import ParseMode
from token_bot import Token

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –±–æ—Ç–∞ –∏ –¥–∏—Å–ø–µ—Ç—á–µ—Ä–∞
bot = Bot(Token.token)
dp = Dispatcher()

@dp.message_handler(commands=['start'])
async def start(message: types.Message):
    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
    btn1 = types.KeyboardButton("üëã –ü–æ–∑–¥–æ—Ä–æ–≤–∞—Ç—å—Å—è")
    markup.add(btn1)
    await message.answer("üëã –ü—Ä–∏–≤–µ—Ç! –Ø —Ç–≤–æ–π –±–æ—Ç-–ø–æ–º–æ—à–Ω–∏–∫!", reply_markup=markup)

@dp.message_handler(lambda message: message.text == "üëã –ü–æ–∑–¥–æ—Ä–æ–≤–∞—Ç—å—Å—è")
async def greet_user(message: types.Message):
    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
    btn1 = types.KeyboardButton('–ö–∞–∫ —Å—Ç–∞—Ç—å –∞–≤—Ç–æ—Ä–æ–º –Ω–∞ –•–∞–±—Ä–µ?')
    btn2 = types.KeyboardButton('–ü—Ä–∞–≤–∏–ª–∞ —Å–∞–π—Ç–∞')
    btn3 = types.KeyboardButton('–°–æ–≤–µ—Ç—ã –ø–æ –æ—Ñ–æ—Ä–º–ª–µ–Ω–∏—é –ø—É–±–ª–∏–∫–∞—Ü–∏–∏')
    markup.add(btn1, btn2, btn3)
    await message.answer('‚ùì –ó–∞–¥–∞–π—Ç–µ –∏–Ω—Ç–µ—Ä–µ—Å—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å', reply_markup=markup)

@dp.message_handler(lambda message: message.text == '–ö–∞–∫ —Å—Ç–∞—Ç—å –∞–≤—Ç–æ—Ä–æ–º –Ω–∞ –•–∞–±—Ä–µ?')
async def become_author(message: types.Message):
    await message.answer('–í—ã –ø–∏—à–µ—Ç–µ –ø–µ—Ä–≤—ã–π –ø–æ—Å—Ç, –µ–≥–æ –ø—Ä–æ–≤–µ—Ä—è—é—Ç –º–æ–¥–µ—Ä–∞—Ç–æ—Ä—ã, –∏, –µ—Å–ª–∏ –≤—Å—ë —Ö–æ—Ä–æ—à–æ, –æ—Ç–ø—Ä–∞–≤–ª—è—é—Ç –≤ –æ—Å–Ω–æ–≤–Ω—É—é –ª–µ–Ω—Ç—É –•–∞–±—Ä–∞, –≥–¥–µ –æ–Ω –Ω–∞–±–∏—Ä–∞–µ—Ç –ø—Ä–æ—Å–º–æ—Ç—Ä—ã, –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ –∏ —Ä–µ–π—Ç–∏–Ω–≥. –í –¥–∞–ª—å–Ω–µ–π—à–µ–º –ø—Ä–µ–º–æ–¥–µ—Ä–∞—Ü–∏—è —É–∂–µ –Ω–µ –ø–æ–Ω–∞–¥–æ–±–∏—Ç—Å—è. –ï—Å–ª–∏ —Å –ø–æ—Å—Ç–æ–º —á—Ç–æ-—Ç–æ –Ω–µ —Ç–∞–∫, –≤–∞—Å –ø–æ–ø—Ä–æ—Å—è—Ç –µ–≥–æ –¥–æ—Ä–∞–±–æ—Ç–∞—Ç—å.\n\n–ü–æ–ª–Ω—ã–π —Ç–µ–∫—Å—Ç –º–æ–∂–Ω–æ –ø—Ä–æ—á–∏—Ç–∞—Ç—å –ø–æ —Å—Å—ã–ª–∫–µ', parse_mode=ParseMode.MARKDOWN)

@dp.message_handler(lambda message: message.text == '–ü—Ä–∞–≤–∏–ª–∞ —Å–∞–π—Ç–∞')
async def site_rules(message: types.Message):
    await message.answer('–ü—Ä–æ—á–∏—Ç–∞—Ç—å –ø—Ä–∞–≤–∏–ª–∞ —Å–∞–π—Ç–∞ –≤—ã –º–æ–∂–µ—Ç–µ –ø–æ —Å—Å—ã–ª–∫–µ', parse_mode=ParseMode.MARKDOWN)

@dp.message_handler(lambda message: message.text == '–°–æ–≤–µ—Ç—ã –ø–æ –æ—Ñ–æ—Ä–º–ª–µ–Ω–∏—é –ø—É–±–ª–∏–∫–∞—Ü–∏–∏')
async def publication_tips(message: types.Message):
    await message.answer('–ü–æ–¥—Ä–æ–±–Ω–æ –ø—Ä–æ —Å–æ–≤–µ—Ç—ã –ø–æ –æ—Ñ–æ—Ä–º–ª–µ–Ω–∏—é –ø—É–±–ª–∏–∫–∞—Ü–∏–π –ø—Ä–æ—á–∏—Ç–∞—Ç—å –ø–æ —Å—Å—ã–ª–∫–µ', parse_mode=ParseMode.MARKDOWN)

async def main():
    await dp.start_polling(bot)

if __name__ == '__main__':  # –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–æ —É—Å–ª–æ–≤–∏–µ
    asyncio.run(main())
